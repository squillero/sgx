def test_choice():
    for _ in range(10000):
        assert randy.choice([0, 1, 2, 3, 4], p=[1, 0, 0, 0, 0]) == 0
        assert randy.choice([0, 1, 2, 3, 4], p=[0, .25, .25, .25, .25]) != 0


def test_shuffled():
    assert list(range(1000)) == sorted(randy.shuffled(list(range(1000))))
